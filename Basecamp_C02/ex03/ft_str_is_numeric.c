int		ft_str_is_numeric(char *str)
{
	int count;
	int i;

	count = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			count = 0;
		}
		i++;
	}
	return (count);
}
