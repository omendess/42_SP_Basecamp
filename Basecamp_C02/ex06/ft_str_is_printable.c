int		ft_str_is_printable(char *str)
{
	int count;
	int i;

	count = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] > 31 && str[i] <= 127))
		{
			count = 0;
		}
		i++;
	}
	return (count);
}
